package com.urunggundrup.gotani.model;

public class ModelSatuan {
    private String id_satuan, nama_satuan, keterangan_satuan, status;

    public String getId_satuan() {
        return id_satuan;
    }

    public void setId_satuan(String id_satuan) {
        this.id_satuan = id_satuan;
    }

    public String getNama_satuan() {
        return nama_satuan;
    }

    public void setNama_satuan(String nama_satuan) {
        this.nama_satuan = nama_satuan;
    }

    public String getKeterangan_satuan() {
        return keterangan_satuan;
    }

    public void setKeterangan_satuan(String keterangan_satuan) {
        this.keterangan_satuan = keterangan_satuan;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
