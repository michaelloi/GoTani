package com.urunggundrup.gotani.model;

public class ModelAlamatUser {
    private String id_alamat, id_user, judul_alamat, alamat, nama_penerima, nohp_penerima, created_date, status_alamat;

    public String getId_alamat() {
        return id_alamat;
    }

    public void setId_alamat(String id_alamat) {
        this.id_alamat = id_alamat;
    }

    public String getId_user() {
        return id_user;
    }

    public void setId_user(String id_user) {
        this.id_user = id_user;
    }

    public String getJudul_alamat() {
        return judul_alamat;
    }

    public void setJudul_alamat(String judul_alamat) {
        this.judul_alamat = judul_alamat;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNama_penerima() {
        return nama_penerima;
    }

    public void setNama_penerima(String nama_penerima) {
        this.nama_penerima = nama_penerima;
    }

    public String getNohp_penerima() {
        return nohp_penerima;
    }

    public void setNohp_penerima(String nohp_penerima) {
        this.nohp_penerima = nohp_penerima;
    }

    public String getCreated_date() {
        return created_date;
    }

    public void setCreated_date(String created_date) {
        this.created_date = created_date;
    }

    public String getStatus_alamat() {
        return status_alamat;
    }

    public void setStatus_alamat(String status_alamat) {
        this.status_alamat = status_alamat;
    }
}
